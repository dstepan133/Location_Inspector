def get_continent(country_name):
    if not country_name:
        return "Unknown"
    
    # Normalize: remove splitters like " / " and take first part
    # e.g. "Bosna i Hercegovina / Босна..." -> "Bosna i Hercegovina"
    name = str(country_name).split('/')[0].strip()
    
    # Direct lookup
    if name in COUNTRY_TO_CONTINENT:
        return COUNTRY_TO_CONTINENT[name]
        
    # Uppercase check (for ISO codes)
    if name.upper() in COUNTRY_TO_CONTINENT:
        return COUNTRY_TO_CONTINENT[name.upper()]
    
    return "Unknown"
